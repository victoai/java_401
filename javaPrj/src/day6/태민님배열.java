package day6;

public class 태민님배열 {

	public static void main(String[] args) {
		String[] team = new String[3];
		team[0] = "맥라렌";
		team[1] = "레드불";
		team[2] = "메르세데스";
		System.out.println(team[0]);
		System.out.println(team[1]);
		System.out.println(team[2]);
		System.out.println();
		for(int i = 0; i < team.length; i++) {
			System.out.println(team[i]);

		}
	}

}
